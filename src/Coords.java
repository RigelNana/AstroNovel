public class Coords {
    private double alpha;
    private double delta;

    public Coords(double alpha, double delta) {
        this.alpha = alpha;
        this.delta = delta;
    }

    public double getAlpha() {
        return alpha;
    }

    public void setAlpha(double alpha) {
        this.alpha = alpha;
    }

    public double getDelta() {
        return delta;
    }

    public void setDelta(double delta) {
        this.delta = delta;
    }
}
